"""
Function taking a list of 8bits
And returns a string from the concatenation of the list's elements
"""


def eight_bit_list_to_string(eight_bit_list):
    # This function takes the list as parameter
    # Then creates a string concatenating the elements
    eight_bit_string = ""
    print(eight_bit_string.join(eight_bit_list))
    return eight_bit_string.join(eight_bit_list)
